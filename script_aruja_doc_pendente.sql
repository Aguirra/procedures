DROP PROCEDURE IF EXISTS P_ATUALIZA_BKP_DOCPED;
CREATE PROCEDURE P_ATUALIZA_BKP_DOCPED ()
	
define vcodme, vcodsap varchar(255);
define i int;
define vtransacao int;
define vstatus int;
define vcodigo int;

let i = 0;

begin work;
let vtransacao = 1;
	foreach WITH hold
		SELECT pedido
		INTO vcodme
		FROM bkp_inc0275683_20200527
		
		SELECT count (*), documento
		INTO vstatus, vcodigo
		FROM esmovcad
		WHERE documento = vcodme
		AND tipo_documento = 'PED'
		GROUP BY documento;

	if vstatus > 0 THEN
		--ATUALIZAÇÃO DO PEDIDO PENDENTE PARA N
			-- E JÁ ATUALIZANDO OS PRODUTOS PARA PENDENTE = N
		UPDATE esmovcad SET pend_sit_mot = 'N' where documento = vcodme AND tipo_documento = 'PED';
		UPDATE esmovite SET pendente = 'N'  where documento = vcodme AND tipo_documento = 'PED';

		update bkp_inc0275683_20200527
		SET ds_atualizacao = 'ESMOVCAD | ESMOVITE Atualizadas ' || to_char(current, '%d/%m/%Y %H:%M') ||'.' 
		WHERE pedido = vcodme;

		else
			UPDATE bkp_inc0275683_20200527
			SET ds_atualizacao = 'Pedido e documento não atualizado.'
			WHERE pedido = vcodme;

	end IF;
	
	if i = 1000 THEN
		IF vtransacao = 1 THEN
			COMMIT work;

			let vtransacao = 0;
			let i = 0;
		end IF;
		if vtransacao = 0 THEN
			BEGIN work;
			let vtransacao = 1;
		end IF;
	end IF;
		let i = i+1;
 end foreach;
	if vtransacao = 1 THEN
			COMMIT work;
			let vtransacao = 0;
			let i = 0;
		end IF;
end PROCEDURE;

