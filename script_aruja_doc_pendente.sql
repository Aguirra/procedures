DROP PROCEDURE IF EXISTS P_TAB_BKP_DOCPED;
CREATE PROCEDURE P_TAB_BKP_DOCPED ()

	define vtable int;

	select count(*)
	into vtable
	from systables
	where upper(tabname) = 'BKP_INC0275683_20200527';

	IF  vtable = 0 then 

		create TABLE BKP_INC0275683_20200527 (
		pedido	varchar(10),
		p_descr	varchar(50),
		ds_atualizacao varchar(255));

	END IF;

END PROCEDURE;

