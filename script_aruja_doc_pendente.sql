DROP PROCEDURE IF EXISTS P_INSERT_BKP_DOCPED;
CREATE PROCEDURE P_INSERT_BKP_DOCPED (
			peddoc varchar(10),
			descr varchar(50)
			)
	
	define vcount int;
	
BEGIN WORK;
		
	SELECT COUNT(*) 
	INTO vcount
	FROM BKP_INC0275683_20200527
	WHERE pedido = peddoc;

		IF vcount = 0 THEN 
	
			INSERT INTO BKP_INC0275683_20200527(pedido, p_descr)
		 	            VALUES(peddoc, descr);
		END IF;
	
	commit work;	
END PROCEDURE;

